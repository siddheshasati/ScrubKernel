def generate_project(prompt: str, owner: str = "local_user") -> dict:
    slug = infer_project_slug(prompt)
    project_dir = next_available_project_dir(slug)
    actual_slug = project_dir.name
    project_dir.mkdir(parents=True, exist_ok=True)

    files = banking_app_files(prompt) if "bank" in prompt.lower() else generic_app_files(prompt)
    written = []
    for relative_path, contents in files.items():
        target = project_dir / relative_path
        target.parent.mkdir(parents=True, exist_ok=True)
        target.write_text(contents, encoding="utf-8")
        written.append(relative_path)

    manifest = {
        "project": actual_slug,
        "owner": owner,
        "prompt": prompt,
        "path": str(project_dir),
        "workspace_path": f"generated_projects/{actual_slug}",
        "command_cwd": actual_slug,
        "files": written,
        "setup_command": "python -m pip install -r requirements.txt",
        "run_command": "streamlit run app.py",
    }
    (project_dir / "agent_manifest.json").write_text(json.dumps(manifest, indent=2), encoding="utf-8")
    return manifest
