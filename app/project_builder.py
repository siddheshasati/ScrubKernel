def slugify(value: str, fallback: str = "generated_app") -> str:
    value = re.sub(r"[^a-zA-Z0-9]+", "_", value.lower()).strip("_")
    return value[:60] or fallback


def infer_project_slug(prompt: str) -> str:
    lowered = prompt.lower()
    if "bank" in lowered:
        return "banking_application"
    if "todo" in lowered or "task" in lowered:
        return "task_manager"
    words = re.findall(r"[a-zA-Z0-9]+", prompt.lower())
    useful = [word for word in words if word not in {"create", "make", "build", "an", "a", "the", "application", "app"}]
    return slugify("_".join(useful[:4]), "generated_app")


def next_available_project_dir(slug: str) -> Path:
    project_dir = PROJECTS_DIR / slug
    if not project_dir.exists():
        return project_dir

    index = 2
    while True:
        candidate = PROJECTS_DIR / f"{slug}_{index}"
        if not candidate.exists():
            return candidate
        index += 1


